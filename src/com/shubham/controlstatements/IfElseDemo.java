package com.shubham.controlstatements;

public class IfElseDemo {

    public static void main(String[] args) {
        int num1 = 10;

        if (num1 < 20) {
            System.out.println("Num 1 is smaller than 20");
        } else {
            System.out.println("Num 1 is greater than 20");
        }
    }
}
