package com.shubham.datatypes;

class ShortProgram {

	public static void main(String[] args) {

		short x = Short.MAX_VALUE; //fetches max value from the Short Wrapper Class

		short y = Short.MIN_VALUE;

		System.out.println(x);
		System.out.println(y);

		x++;
		y--;
		System.out.println(x);
		System.out.println(y);

	}

}


